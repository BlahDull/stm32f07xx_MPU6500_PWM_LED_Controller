long map(long x, long in_min, long in_max, long out_min, long out_max) {
	long res = (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
	if (res > out_max) return out_max;
	if (res < out_min) return out_min;
	return res;
}
