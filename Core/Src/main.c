void Sensor_Read_Task(void *argument)
{
  /* USER CODE BEGIN Sensor_Read_Task */
  /* Infinite loop */
	MPU6500_Accelerometer_Data_t accel_data = {0};
	uint32_t accel_data_y_z = 0;

  for(;;)
  {
	if (MPU6500_Read_Accelerometer_Data(&hi2c1, MPU6500_I2C_ADDR, &accel_data) != MPU6500_OK) {
		Error_Handler();
	}
	accel_data = MPU6500_Accelerometer_Calibration(&g_error_data, &accel_data);
	accel_data_y_z = accel_data.y;
	accel_data_y_z = (accel_data_y_z << 16) | accel_data.z;
	// send notif to pwm task
	xTaskNotify((TaskHandle_t)PWM_ControlHandle, accel_data_y_z, eSetValueWithOverwrite);
	xTaskNotifyWait(0, 0, NULL, portMAX_DELAY);
	// enter blocked
  }
  /* USER CODE END Sensor_Read_Task */
}

/* USER CODE BEGIN Header_PWM_Control_Task */
/**
* @brief Function implementing the PWM_Control_Tas thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_PWM_Control_Task */
void PWM_Control_Task(void *argument)
{
  /* USER CODE BEGIN PWM_Control_Task */
	/* Infinite loop */
	double roll_angle = 0, kalman_roll_angle = 0, delta_time = 0;
	uint32_t prev_tick = osKernelGetTickCount(), accel_data_y_z;
	MPU6500_Accelerometer_Data_t accel_data;
	KalmanFilter kf;
	kalman_filter_init(&kf);
	for(;;)
	{
		xTaskNotifyWait(0, ULONG_MAX, &accel_data_y_z, portMAX_DELAY);

		accel_data.z = (int16_t) accel_data_y_z & 0xFFFF;
		accel_data.y = (int16_t) (accel_data_y_z >> 16) & 0xFFFF;

		uint32_t current_tick = osKernelGetTickCount();
		delta_time = (current_tick - prev_tick) / 1000.0;
		prev_tick = current_tick;
		g_counter = __HAL_TIM_GET_COUNTER(&htim4);


		roll_angle = atan2(accel_data.y, accel_data.z) * (180.0 / M_PI);
		kalman_roll_angle = kalman_filter_get_angle(&kf, roll_angle, delta_time);
		int16_t filtered_angle = (int16_t) kalman_roll_angle;
		uint8_t channel = (filtered_angle < 0) ? TIM_CHANNEL_1 : TIM_CHANNEL_2;
		filtered_angle = abs(filtered_angle);
		uint32_t pwm_pulse = map(filtered_angle, ANGLE_POS_MIN, ANGLE_POX_MAX, PWM_PULSE_MIN, PWM_PULSE_MAX);
		__HAL_TIM_SET_COMPARE(&htim4, channel, pwm_pulse);
		//HAL_NVIC_EnableIRQ(EXTI9_5_IRQn);
	}
  /* USER CODE END PWM_Control_Task */
}

